package my.random.bean;


public class CustomSetting {
    int odd_per;
    int even_per;
    int continuity_per;
    int lastnum_per;
    int exceptLocation;
    public int getOdd_per() {
        return odd_per;
    }
    public void setOdd_per(int odd_per) {
        this.odd_per = odd_per;
    }
    public int getEven_per() {
        return even_per;
    }
    public void setEven_per(int even_per) {
        this.even_per = even_per;
    }
    public int getContinuity_per() {
        return continuity_per;
    }
    public void setContinuity_per(int continuity_per) {
        this.continuity_per = continuity_per;
    }
    public int getLastnum_per() {
        return lastnum_per;
    }
    public void setLastnum_per(int lastnum_per) {
        this.lastnum_per = lastnum_per;
    }
    public int getExceptLocation() {
        return exceptLocation;
    }
    public void setExceptLocation(int exceptLocation) {
        this.exceptLocation = exceptLocation;
    }


}
